class Pokemon:
    def __init__(self, name, power):
        self.name = name  # 이름
        self.power = power  # 전투력

    def __repr__(self):
        return self.name



def my_sort(arr):
    n = len(arr)
    for end in range(n - 1, 0, -1):
        for cur in range(0, end):
            if (arr[cur] > arr[cur + 1]):
                arr[cur], arr[cur + 1] = arr[cur + 1], arr[cur]
    return arr


def ur_sort(arr):
    n = len(arr)
    for i in range(0, n - 1):
        idx = i
        for k in range(i + 1, n):
            if (arr[idx] > arr[k]):
                idx = k
        tmp = arr[i]
        arr[i] = arr[idx]
        arr[idx] = tmp

    return arr


